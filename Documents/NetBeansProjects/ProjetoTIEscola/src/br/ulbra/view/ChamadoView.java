    public void listarTabela() {
        DefaultTableModel model = (DefaultTableModel) tblChamado.getModel();
        model.setRowCount(0);

        for (Chamado u : controller.listar()) {
            model.addRow(new Object[]{
                u.getId(),
                u.getSolicitante(),
                u.getSala(),
                u.getEquipamentoTag(),
                u.getProblemaRelatado(),
                u.getDiagnosticoTecnico(),
                u.getPrioridade(),
                u.getStatus()
            });
        }
    }

